package chapter3;

public class OperationEx4 {

	public static void main(String[] args) {
		int a = 1;
		int b = 5;
		
		boolean c = (a>0)&&(b>0);
		System.out.println(c);
		
		boolean d = (a>0)||(b>0);
		System.out.println(d);
		
		boolean e = !(a<b);
		System.out.println(e);
		
	}

}
